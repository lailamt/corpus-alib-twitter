def clean(text):
    text = re.sub(r'\\n', '\n', str(text))
    text = re.sub(r'\n', ' ', str(text))

    # coloca todas as letras em minúsculo
    text = text.lower()
    # substitui os caracteres _ por espaço em branco
    text = re.sub(r'_+', ' ', str(text))
    # remove caracteres especiais e pontuações
    text = re.sub(r'[^\w\s]', '', str(text))
    # substitui as versões acentuadas das vogais pela vogal sem acento
    text = re.sub(r'[àáâãäå]', 'a', str(text))
    text = re.sub(r'[èéêë]', 'e', str(text))
    text = re.sub(r'[ìíîï]', 'i', str(text))
    text = re.sub(r'[òóôõöø]', 'o', str(text))
    text = re.sub(r'[ùúûü]', 'u', str(text))
    # remove 'kk...' ou 'haha...'
    # text = re.sub(r'k{2,}|h{2,}', '', str(text))
    text = re.sub(r'(k{2,})', 'k', str(text)) # sequências de k's em palavras
    text = re.sub(r'(\bk{2,}\b)', '', str(text)) # sequências de k's como palavras
    # text = re.sub(r'h(a|h)*', '', str(text)) # sequências de ha's em palavras
    text = re.sub(r'\bk(a|k)*\b', '', str(text)) # sequências de ka's como palavras
    text = re.sub(r'\bh(a|h)*\b', '', str(text)) # sequências de ha's como palavras
    # remove urls
    text = re.sub(r'http\S+', '', str(text))
    # remove menções a usuários
    text = re.sub(r'@\w+', '', str(text))
    # remove hashtags
    text = re.sub(r'#\w+', '', str(text))
    # remove números
    text = re.sub(r'\d+', '', str(text))
    # remove emojis
    emoji_pattern = re.compile("["
                               u"\U0001F600-\U0001F64F"  # emoticons
                               u"\U0001F300-\U0001F5FF"  # symbols & pictographs
                               u"\U0001F680-\U0001F6FF"  # transport & map symbols
                               u"\U0001F1E0-\U0001F1FF"  # flags (iOS)
                               "]+", flags=re.UNICODE)
    text = emoji_pattern.sub(r'', str(text))
    # remove espaços em branco extras
    text = re.sub(r'\s+', ' ', str(text))


    text = [w for w in word_tokenize(text) if (w not in stopwords and len(w)>2)]


    return text

