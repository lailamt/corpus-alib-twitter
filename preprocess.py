def alter_word(df, old_word):
    df.loc[df['date'] <= 2017, 'tweet'] = [
        [old_word.upper()+'20132017' if word == old_word else word for word in words]
        for words in df.loc[df['date'] <= 2017, 'tweet']
    ]
    df.loc[df['date'] > 2017, 'tweet'] = [
        [old_word.upper()+'20182022' if word == old_word else word for word in words]
        for words in df.loc[df['date'] > 2017, 'tweet']
    ]
    return df

